// filter product
export const productFiltersController = async (req, res) => {
  try {
    // we take two value as request checked and radio for filter
    const { checked, radio } = req.body;
    let args = {};
    if (checked.length > 0) {
      args.category = checked;
    }
    if (radio.length) {
      args.price = { $gte: radio[0], $lte: radio[1] }; // $gte is greater than equal to similarly lte
    }
    const products = await productModel.find(args);
    res.status(200).send({
      success: true,
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(400).send({
      success: false,
      message: "error while filtering product",
      error,
    });
  }
};

