//search product
export const searchProductController = async (req, res) => {
  try {
    const { keyword } = req.params; //destructure the keyword, we get it by param of the url
    const results = await productModel
      .find({
        $or: [
          { name: { $regex: keyword, $options: "i" } }, // i means it becomes case insensitive
          { description: { $regex: keyword, $options: "i" } },
        ],
      })
      .select("-photo");
    res.json(results);
  } catch (error) {
    console.log(error);
    res.status(400).send({
      success: false,
      message: "Error in search product API",
      error,
    });
  }
};

